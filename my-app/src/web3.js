// Connect to Ethereum provider (Replace with your actual RPC URL)
const web3 = new Web3('https://open-campus-codex-sepolia.drpc.org');

// Initialize contract instance
const contract = new web3.eth.Contract(contractABI, contractAddress);

export const mintNFT = async (account) => {
    if (!account) {
        console.error('Wallet is not connected');
        alert('Connect Wallet first');
        return;
    }

    try {
        console.log(`Minting NFT for account: ${account}`);
        const tx = await contract.methods.mint(account).send({ from: account });
        console.log('Minted NFT:', tx);
        return tx;
    } catch (error) {
        console.error('Minting failed:', error);
        alert(`Minting failed: ${error.message}`);
    }
};

export async function lockNFT(tokenId) {
    const { address, isConnected } = useWallet();
    if (!isConnected) {
        console.error("Wallet not connected");
        return;
    }

    try {
        const tx = await writeContract({
            address: contractAddress,
            abi: contractABI,
            functionName: 'setLocked',
            args: [tokenId],
            account: address,
        });

        console.log(`Token ${tokenId} locked successfully:`, tx);
    } catch (error) {
        console.error('Error locking NFT:', error);
    }
}

export async function unlockNFT(tokenId) {
    const { address, isConnected } = useWallet();
    if (!isConnected) {
        console.error("Wallet not connected");
        return;
    }

    try {
        const tx = await writeContract({
            address: contractAddress,
            abi: contractABI,
            functionName: 'setUnLocked',
            args: [tokenId],
            account: address,
        });

        console.log(`Token ${tokenId} unlocked successfully:`, tx);
    } catch (error) {
        console.error('Error unlocking NFT:', error);
    }
}